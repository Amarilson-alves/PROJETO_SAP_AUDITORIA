import re
import unicodedata

# Normaliza chave removendo acentos, espaços e uppercasing (mesmo critério de SAPReader.normalize_str)
def _nk(s: str) -> str:
    nfkd = unicodedata.normalize('NFKD', str(s))
    sem_acento = ''.join(c for c in nfkd if not unicodedata.combining(c)).upper()
    return re.sub(r'\s+', '', sem_acento)

